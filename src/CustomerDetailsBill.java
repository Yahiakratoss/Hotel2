    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
   int index = jTable1.getSelectedRow();
    TableModel model = jTable1.getModel();

    String id = model.getValueAt(index, 0).toString();
    String name = model.getValueAt(index, 1).toString();
    String mobileNumber = model.getValueAt(index, 2).toString();
    String email = model.getValueAt(index, 5).toString();
    String roomNo = model.getValueAt(index, 9).toString();
    String bed = model.getValueAt(index, 10).toString();
    String roomType = model.getValueAt(index, 11).toString();
    String pricePerDay = model.getValueAt(index, 12).toString();
    String checkInDate = model.getValueAt(index, 8).toString();
    String checkOutDate = model.getValueAt(index, 15).toString();
    String numberOfDaysStay = model.getValueAt(index, 13).toString();
    String totalAmount = model.getValueAt(index, 14).toString();

    String path = "C:/Users/Public/Documents/"; // Chemin s√ªr pour sauvegarder
    com.itextpdf.text.Document doc = new com.itextpdf.text.Document();

    try {
        PdfWriter.getInstance(doc, new FileOutputStream(path + id + ".pdf"));
        doc.open();

        // Ajouter le contenu au PDF
        Paragraph paragraph1 = new Paragraph("                       Hotel Management System\n");
        doc.add(paragraph1);
        Paragraph paragraph2 = new Paragraph("*************************************************************************************");
        doc.add(paragraph2);
        Paragraph paragraph3 = new Paragraph(
            "\tBill ID: " + id + "\nCustomer Details:\nName: " + name +
            "\nMobile Number: " + mobileNumber + "\nEmail: " + email + "\n"
        );
        doc.add(paragraph3);
        doc.add(paragraph2);
        Paragraph paragraph4 = new Paragraph(
            "\tRoom Details:\nNumber: " + roomNo + "\nType: " + roomType +
            "\nBed: " + bed + "\nPrice Per Day: " + pricePerDay
        );
        doc.add(paragraph4);
        doc.add(paragraph2);

        // Ajouter un tableau avec des d√©tails
        com.itextpdf.text.pdf.PdfPTable tb1 = new com.itextpdf.text.pdf.PdfPTable(4);
        tb1.addCell("Check In Date: " + checkInDate);
        tb1.addCell("Check OUT Date: " + checkOutDate);
        tb1.addCell("No of Day Stay: " + numberOfDaysStay);
        tb1.addCell("Total Amount Paid: " + totalAmount);
        doc.add(tb1);

        doc.add(paragraph2);
        Paragraph paragraph5 = new Paragraph("Thank you, Please Visit Again.");
        doc.add(paragraph5);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erreur lors de la g√©n√©ration du PDF : " + e.getMessage());
    } finally {
        doc.close();
    }

    // Demander confirmation pour imprimer le PDF
    int a = JOptionPane.showConfirmDialog(null, "Do you want to print the bill?", "Select", JOptionPane.YES_NO_OPTION);
    if (a == 0) {
        try {
            File pdfFile = new File(path + id + ".pdf");
            if (pdfFile.exists()) {
                Desktop.getDesktop().open(pdfFile); // Ouvrir le fichier PDF
            } else {
                JOptionPane.showMessageDialog(null, "File does not exist: " + pdfFile.getAbsolutePath());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur lors de l'ouverture du PDF : " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jTable1MouseClicked

